# Запрос данных у пользователя
first_num = int(input("Введите первое целое число: "))
second_num = int(input("Введите второе целое число: "))

# выводим результаты операций
print("Сумма чисел: " + str(first_num + second_num))
print("Разность чисел: " + str(first_num - second_num))
print("Произведение чисел: " + str(first_num * second_num))
print("частное деления первого числа на второе: " + str(first_num / second_num))
print("Остаток деления первого числа на второе: " + str(first_num % second_num))
print("Возведение первого числа в степень второго: " + str(first_num ** second_num))